#Arrays in python

shoppingList = ['Milk', 'Bread']

print("The shopping list contains:", shoppingList)

shoppingList.append('Meat')
shoppingList.append('Chicken')
shoppingList.append('Snacks')
shoppingList.append('Rice')
shoppingList.append('Eggs')
print("The shopping list after appnding some items:", shoppingList)

shoppingList.reverse()
print("The shopping list after reversing its items:", shoppingList)

shoppingList.sort()
print("The shopping list after sorting its items:", shoppingList)


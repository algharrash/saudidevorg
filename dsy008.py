#Python String Methods

x = "   My Name:   "
my_name = "Ali Ahmad AlGharrash"

#print the length of string
print(len(my_name))

#remove whitespaces from the string
print(x.strip())

#convert strin to uppercase
print(my_name.upper())

#convert string to lowercase
print(my_name.lower())

#split the string
print(my_name.split(" "))

#replace all " " with "," in string
print(my_name.replace(" ", ","))